function setPictureYellow() {
    document.getElementById("profile-dropdown-pic").src = '../images/icons/yellow profile icon.png'
}

function setPictureWhite() {
    document.getElementById("profile-dropdown-pic").src = '../images/icons/white profile icon.png'
}

function setBookmark() {
    let imgPath = document.getElementById("bookmarkIcon").src
    if (imgPath.includes('bookmark-white.png')) {
        document.getElementById("bookmarkIcon").src = '../images/icons/bookmark-yellow.png'
    } else if (imgPath.includes('bookmark-yellow.png')) {
        document.getElementById("bookmarkIcon").src = '../images/icons/bookmark-white.png'
    }
}

